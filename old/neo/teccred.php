<h2>Sobre o Teccred</h2><br/>

<p>Coordenado pelo Prof. Dr. Walter Lima, o Teccred - Grupo de Pesquisa Comunicação, Tecnologia e Cultura da Rede, pertencente ao Programa de Mestrado da Cásper Líbero - analisa a evolução e os processos tecnológicos, os seus impactos e a sua organização em uma sociedade informacional conectada, tendo como foco a relação entre comunicação, tecnologia e cidadania.</p><br/>

<p>Os pesquisadores têm como objetivos captar o cotidiano em mutação, em virtude das transformações sociais, culturais e comportamentais decorrentes das tecnologias digitais conectadas, pesquisando os processos de governança informacional, as redes sociais, as mídias sociais, as práticas colaborativas, o áudiovisual multimídia interativo, as mudanças nos direitos individuais e coletivos, a mobilidade, as interfaces digitais e os games.</p><br/>

<p>O grupo conta com o trabalho integrado de professores e alunos da Faculdade Cásper Líbero, além de pesquisadores convidados de organizações da sociedade civil.</p><br/>

