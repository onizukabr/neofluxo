<h2>Conheça todos os nossos bancos de dados disponíveis</h2><br/>

<p>Durante o primeiro e segundo turnos das eleições presidenciais, o sistema NeoFluxo coletou dados de fontes oficiais e menções específicas no Twitter. Estes foram organizados em bancos de dados diferentes, todas acessíveis a partir de uma API, o que permite a qualquer desenvolvedor criar e visualizar possíveis relações entre elas.</p><br/><br/>

