<h2>Databases</h2><br/>

<p>During the first and second votes of the Presidential election, the NeoFluxo system collected data from official sources and specific terms on Twitter. These were organized in different databases, all accessible through an API, which allows any developer to create and visualize possible relationships between them.</p><br/><br/>

<h4 id="tt">Data collected from the Official Channels:</h4><br/>

<ul>
<li>- All updates from the official channels of candidates Dilma Rouseff and Jose Serra on the Internet, from Twitter, Facebook, YouTube, Flickr and the official websites (no filter):</li><br/>
		<ul>
		   <li>:: First vote: between 09/11/2010 and 10/03/2010</li>
		   <li>:: Second vote: between 10/08/2010 and 10/29/2010</li>
		</ul>
<br/>	
<li>- All updates from the official channels of candidate Marina Silva on the Internet, from Twitter, Facebook, YouTube, Flickr and the official website (no filter);</li><br/>
	<ul>
		<li>:: First vote: between 09/23/2010 and 10/03/2010</li>
	</ul>
</ul>
<br/>
<br/>
